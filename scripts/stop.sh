#!/bin/bash
# ==============================================================================
# 停止开发环境脚本
# 用途：停止Docker服务
# ==============================================================================

set -e

# 颜色定义
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

# 项目根目录
PROJECT_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$PROJECT_ROOT"

# ==============================================================================
# (1) 停止Docker服务
# ==============================================================================
echo -e "${YELLOW}正在停止Docker服务...${NC}"
if docker compose version &> /dev/null; then
    docker compose down
else
    docker-compose down
fi

echo -e "${GREEN}✓${NC} 服务已停止"
