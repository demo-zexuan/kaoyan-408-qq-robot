#!/bin/bash
# ==============================================================================
# 启动开发环境脚本
# 用途：启动Docker服务（Redis）
# ==============================================================================

set -e  # 遇到错误立即退出

# 颜色定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# 项目根目录
PROJECT_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$PROJECT_ROOT"

# ==============================================================================
# (1) 打印欢迎信息
# ==============================================================================
echo -e "${GREEN}============================================${NC}"
echo -e "${GREEN}    考研408 QQ Robot - 开发环境启动${NC}"
echo -e "${GREEN}============================================${NC}"
echo ""

# ==============================================================================
# (2) 检查Docker是否安装
# ==============================================================================
if ! command -v docker &> /dev/null; then
    echo -e "${RED}错误: 未找到Docker，请先安装Docker${NC}"
    exit 1
fi

if ! command -v docker-compose &> /dev/null && ! docker compose version &> /dev/null; then
    echo -e "${RED}错误: 未找到docker-compose${NC}"
    exit 1
fi

# ==============================================================================
# (3) 创建必要的目录
# ==============================================================================
echo -e "${YELLOW}[1/4]${NC} 创建必要的目录..."
mkdir -p data/db
mkdir -p data/redis
mkdir -p logs
echo -e "${GREEN}✓${NC} 目录创建完成"

# ==============================================================================
# (4) 检查.env文件
# ==============================================================================
echo -e "${YELLOW}[2/4]${NC} 检查配置文件..."
if [ ! -f ".env" ]; then
    echo -e "${RED}警告: .env文件不存在，正在从.env.example创建...${NC}"
    if [ -f ".env.example" ]; then
        cp .env.example .env
    else
        echo -e "${RED}错误: .env.example文件也不存在${NC}"
        exit 1
    fi
fi
echo -e "${GREEN}✓${NC} 配置文件检查完成"

# ==============================================================================
# (5) 启动Docker服务
# ==============================================================================
echo -e "${YELLOW}[3/4]${NC} 启动Docker服务..."
if docker compose version &> /dev/null; then
    docker compose up -d
else
    docker-compose up -d
fi
echo -e "${GREEN}✓${NC} Docker服务启动完成"

# ==============================================================================
# (6) 等待服务就绪
# ==============================================================================
echo -e "${YELLOW}[4/4]${NC} 等待服务就绪..."
sleep 3

# 检查Redis是否就绪
if docker exec kaoyan-408-redis redis-cli ping &> /dev/null; then
    echo -e "${GREEN}✓${NC} Redis服务就绪"
else
    echo -e "${YELLOW}⚠${NC} Redis服务可能需要更多时间启动"
fi

# ==============================================================================
# (7) 显示服务信息
# ==============================================================================
echo ""
echo -e "${GREEN}============================================${NC}"
echo -e "${GREEN}    服务启动完成！${NC}"
echo -e "${GREEN}============================================${NC}"
echo ""
echo -e "Redis服务:"
echo -e "  - 地址: localhost:6379"
echo -e "  - 管理界面: http://localhost:8081"
echo ""
echo -e "常用命令:"
echo -e "  - 停止服务: ./scripts/stop.sh"
echo -e "  - 查看日志: docker compose logs -f"
echo -e "  - 重启服务: docker compose restart"
echo ""
